package cmm02.op;

public class No01_DanHangDemo {
	public static void main(String[] args) {
		//int i;   //변수의 선언(declare)
		//i = 5;   //초기화,할당(allocation)
		int i = 5;
		i = i + 1;
		System.out.println("i = i + 5 의 값 : " + i);
		
		int j =5;
		j++;
		System.out.println("j++ 의 값 : " + j);
		
		int k = 5;
		++k;
		System.out.println("++k 의 값 : " + k);
		
	}
}
